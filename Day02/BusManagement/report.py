def print_report(
    plate,
    route,
    seats,
    sold_tickets,
    empty_seats,
    ticket_price,
    revenue
):

    print("\n  BUS REPORT ")

    print("License Plate :", plate)

    print("Route         :", route)

    print("Total Seats   :", seats)

    print("Sold Tickets  :", sold_tickets)

    print("Empty Seats   :", empty_seats)

    print("Ticket Price  :", ticket_price, "vnd")

    print("Revenue      :", revenue, "vnd")

